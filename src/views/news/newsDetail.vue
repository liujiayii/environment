      来自国家能源局的数据显示，今年上半年，光伏装机和发电量屡创新高的同时，总体弃光率、弃光电量实现双降。全国光伏发电量823.9亿千瓦时，同比增长达59%；全国弃光率3.6%，同比下降3.2个百分点，弃光电量30.4亿千瓦，同比下降7.1亿千瓦时，弃光问题逐步好转，“双升双降”间标志着开发效益、能源利用效率的不断提高。

