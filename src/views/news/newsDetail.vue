      据熊敏峰介绍，随着光伏行业技术进步、产业升级，光伏组件价格不断下降，光伏发电成本逐步下降，市场竞争力持续增强。第三期应用领跑基地的中标电价较标杆电价平均下降了0.24元/千瓦时，平均降幅为36.7%；最低电价达到0.31元/千瓦时，成为国内第一个低于当地燃煤标杆电价的光伏电站。

