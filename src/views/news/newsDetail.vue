      回顾刚刚过去的半年，为促进光伏发电高质量发展，国家能源局会同有关方面进一步完善光伏发电政策体系：一是着力优化光伏发电发展环境；二是扎实有序开展光伏扶贫；三是积极推进领跑基地建设。

      记者了解到，今年上半年，国家能源局通过开展光伏发电专项监管工作、建立市场环境监测评价机制、出台减轻可再生能源领域企业投资经营负担政策文件等，加大行业监管力度，引导企业理性投资，优化行业发展环境；落实精准扶贫、精准脱贫要求，规范和加强光伏扶贫管理，出台《光伏扶贫电站管理办法》和《进一步支持贫困地区能源发展助推脱贫攻坚行动方案（2018-2020年）》，助推贫困地区脱贫攻坚；指导、督促地方完成第三期领跑基地的企业竞争优选工作，确保领跑基地实施效果，目前应用领跑基地已陆续开工建设，技术领跑基地建设工作有序推进。

      此前，国家能源局表示，未来还将坚定不移支持光伏发展，继续与有关各方一道，促进光伏产业持续健康有序发展。“从单一发展规模转向高质量增长，是光伏行业当前的发展使命，也是国家能源局着力推动的发展方向。”谈及高质量发展，熊敏峰向与会者透露，“将光伏发展重心从扩大规模转到提质增效上来，即转重规模扩张为重发展质量，优化规模管理，保持合理发展节奏。”

      此外，国家能源局还将努力减轻企业负担，推动地方做好《关于减轻可再生能源领域企业负担有关事项的通知》的实施落实，使措施要求落到实处，真正让光伏企业轻装前行，为光伏企业营造良好营商环境。

