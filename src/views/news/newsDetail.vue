      7月26日，光伏行业2018年上半年发展回顾与下半年形势展望研讨会在京召开，国家能源局新能源和再生能源司新能源处处长熊敏峰在致辞中表示，今年上半年分布式光伏新增装机1224万千瓦，同比增长72%，新增规模首次超过集中式光伏，体现了光伏发电未来发展方向。

